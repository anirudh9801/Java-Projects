package com.java.proj;

public class ToString {
    public static void main(String[] args) {
        // Declare and initialize an Integer object with the value 100.
        Integer myInt = 100;
        
        // Convert the Integer to a String using the toString() method and store it in myString.
        String myString = myInt.toString();
        
        // Print the length (number of characters) of the myString to the console.
        System.out.println(myString.length());
    }
}
